public class RepeatYourself {

    public static void main(String[] args) {
        // for loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("Count: " + i);
        }

        // while loop
        int x = 3;
        while (x > 0) {
            System.out.println("x is: " + x);
            x--;
        }
    }
}
